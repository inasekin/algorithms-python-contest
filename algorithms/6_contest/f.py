"""
Задача о билетах на мюзикл с оптимизацией очереди.

N человек в очереди, каждый хочет купить 1 билет. Кассир может продать не более 3 билетов в одни руки.
Люди могут объединяться в группы по 2-3 человека, и первый из группы покупает билеты на всех.

Время обслуживания зависит от количества билетов:
- 1 билет: A_i секунд
- 2 билета: B_i секунд
- 3 билета: C_i секунд

Нужно найти минимальное время для обслуживания всех покупателей.

Решение через динамическое программирование:
- dp[i] = минимальное время для обслуживания первых i человек
- dp[0] = 0 (никого не обслужили)
- Для каждого человека есть 3 варианта:
  1) Он покупает сам: dp[i] = dp[i-1] + A[i-1]
  2) Он объединяется со следующим: dp[i] = dp[i-2] + B[i-2]
  3) Он объединяется с двумя следующими: dp[i] = dp[i-3] + C[i-3]
- Выбираем минимум из доступных вариантов

Сложность: O(N)
"""

n = int(input())
a = []
b = []
c = []

for _ in range(n):
    times = list(map(int, input().split()))
    a.append(times[0])
    b.append(times[1])
    c.append(times[2])

dp = [float('inf')] * (n + 1)
dp[0] = 0

for i in range(1, n + 1):
    dp[i] = dp[i - 1] + a[i - 1]

    if i >= 2:
        dp[i] = min(dp[i], dp[i - 2] + b[i - 2])

    if i >= 3:
        dp[i] = min(dp[i], dp[i - 3] + c[i - 3])

print(dp[n])