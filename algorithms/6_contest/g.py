"""
Задача о выборе бургеров с ограничениями.

Лосяш готовит N видов бургеров и выставляет их на раздачу по порядку.
Если он берет бургер, то следующие M раздач пропускает.
Каждый бургер дает удовольствие H_i.
Нужно найти максимальное суммарное удовольствие.

Решение через динамическое программирование:
- dp[i] = максимальное удовольствие от первых i бургеров
- dp[0] = 0 (ничего не взяли)
- Для каждого бургера i есть два варианта:
  1) Не брать его: dp[i] = dp[i-1]
  2) Взять его: dp[i] = dp[max(0, i-M-1)] + H[i-1]
     (взяв i-й бургер, мы пропускаем следующие M, поэтому следующий доступен на позиции i+M+1,
      а значит нужно смотреть на состояние до взятия текущего: i-M-1)
- Выбираем максимум из двух вариантов

Сложность: O(N)
"""

n, m = map(int, input().split())
h = []
for _ in range(n):
    h.append(int(input()))

dp = [0] * (n + 1)

for i in range(1, n + 1):
    dp[i] = dp[i - 1]

    prev_index = max(0, i - m - 1)
    dp[i] = max(dp[i], dp[prev_index] + h[i - 1])

print(dp[n])