"""
Задача о нахождении длины наибольшей общей подпоследовательности (НОП/LCS).

Даны две последовательности длиной N и M.
Подпоследовательность - это то, что можно получить вычеркиванием некоторых элементов.
Нужно найти длину наибольшей общей подпоследовательности.

Решение через динамическое программирование:
- dp[i][j] = длина НОП для первых i элементов первой последовательности
             и первых j элементов второй последовательности
- dp[0][j] = 0, dp[i][0] = 0 (пустые последовательности)
- Если a[i-1] == b[j-1], то элементы совпадают:
  dp[i][j] = dp[i-1][j-1] + 1 (добавляем этот элемент к НОП)
- Иначе выбираем максимум из двух вариантов:
  dp[i][j] = max(dp[i-1][j], dp[i][j-1])
  (либо не берем i-й элемент из первой, либо j-й из второй)

Сложность: O(N * M)
"""

n = int(input())
a = list(map(int, input().split()))
m = int(input())
b = list(map(int, input().split()))

dp = [[0] * (m + 1) for _ in range(n + 1)]

for i in range(1, n + 1):
    for j in range(1, m + 1):
        if a[i-1] == b[j-1]:
            dp[i][j] = dp[i-1][j-1] + 1
        else:
            dp[i][j] = max(dp[i-1][j], dp[i][j-1])

print(dp[n][m])