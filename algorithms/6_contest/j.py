"""
Задача о расписании взвешенных интервалов (Weighted Interval Scheduling).

Дано n интервалов с началом b_i, концом e_i и весом w_i.
Интервалы не включают концы: [b_i, e_i).
Множество совместимо, если никакие два интервала не пересекаются.
Нужно найти совместимое подмножество с максимальным суммарным весом.

Решение через динамическое программирование:
1. Сортируем интервалы по времени окончания
2. Для каждого интервала находим последний непересекающийся интервал до него
   (интервалы [a,b) и [c,d) не пересекаются, если b <= c или d <= a)
3. dp[i] = максимальный вес при рассмотрении первых i интервалов
4. Для каждого интервала i есть два варианта:
   - Не брать: dp[i] = dp[i-1]
   - Взять: dp[i] = w[i] + dp[p[i]], где p[i] - индекс последнего непересекающегося
5. Выбираем максимум

Сложность: O(n²) или O(n log n) с бинарным поиском
"""

n = int(input())
intervals = []
for _ in range(n):
    b, e, w = map(float, input().split())
    intervals.append((b, e, w))

intervals.sort(key=lambda x: x[1])

dp = [0.0] * (n + 1)

for i in range(1, n + 1):
    b_i, e_i, w_i = intervals[i - 1]

    dp[i] = dp[i - 1]

    p = 0
    for j in range(i - 1, 0, -1):
        if intervals[j - 1][1] <= b_i:
            p = j
            break

    dp[i] = max(dp[i], w_i + dp[p])

print(f"{dp[n]:.4f}")