"""
Задача о черепашке, собирающей числа на таблице N×M.

Черепашка стартует из левого верхнего угла (0,0) и должна дойти до правого нижнего угла (N-1, M-1).
Она может двигаться только вправо или вниз и собирает числа в клетках, через которые проходит.
Нужно найти максимальную сумму чисел на пути.

Решение через динамическое программирование:
- dp[i][j] = максимальная сумма чисел на пути до клетки (i, j)
- dp[0][0] = table[0][0] (начальная клетка)
- В клетку (i, j) можно прийти либо сверху (i-1, j), либо слева (i, j-1)
- dp[i][j] = table[i][j] + max(dp[i-1][j], dp[i][j-1])
- Для первой строки можно идти только слева, для первого столбца - только сверху

Сложность: O(N * M)
"""

n, m = map(int, input().split())
table = []
for _ in range(n):
    row = list(map(int, input().split()))
    table.append(row)

dp = [[0] * m for _ in range(n)]
dp[0][0] = table[0][0]

for j in range(1, m):
    dp[0][j] = dp[0][j-1] + table[0][j]

for i in range(1, n):
    dp[i][0] = dp[i-1][0] + table[i][0]

for i in range(1, n):
    for j in range(1, m):
        dp[i][j] = table[i][j] + max(dp[i-1][j], dp[i][j-1])

print(dp[n-1][m-1])