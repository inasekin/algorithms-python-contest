"""
Задача о кузнечике на доске длиной N клеток.

Кузнечик стартует с первой клетки и может прыгать на 1, 2, ..., k клеток вперед.
Нужно найти количество способов допрыгать до последней клетки.

Решение через динамическое программирование:
- dp[i] = количество способов добраться до клетки i
- dp[1] = 1 (начальная позиция)
- dp[i] = dp[i-1] + dp[i-2] + ... + dp[i-k]
  (сумма всех способов добраться до клеток, с которых можно допрыгнуть до i)

Для каждой клетки i суммируем способы добраться до всех клеток от (i-k) до (i-1),
с которых можно допрыгнуть до i одним прыжком.

Сложность: O(N * k)
"""

n, k = map(int, input().split())

dp = [0] * (n + 1)
dp[1] = 1

for i in range(2, n + 1):
    for jump in range(1, k + 1):
        if i - jump >= 1:
            dp[i] += dp[i - jump]

print(dp[n])