"""
Задача о ходе шахматного коня на доске N×M.

Конь стартует из левого верхнего угла (0,0) и должен попасть в правый нижний угол (N-1, M-1).
Конь может ходить ТОЛЬКО двумя способами:
- на 2 клетки вниз и 1 клетку вправо
- на 1 клетку вниз и 2 клетки вправо

Решение через динамическое программирование:
- dp[i][j] = количество способов добраться до клетки (i, j)
- dp[0][0] = 1 (начальная позиция)
- В клетку (i, j) можно прийти из:
  * (i-2, j-1) - если сделали ход "2 вниз, 1 вправо"
  * (i-1, j-2) - если сделали ход "1 вниз, 2 вправо"
- dp[i][j] = dp[i-2][j-1] + dp[i-1][j-2] (с проверкой границ)

Сложность: O(N * M)
"""

n, m = map(int, input().split())

dp = [[0] * m for _ in range(n)]
dp[0][0] = 1

for i in range(n):
    for j in range(m):
        if i >= 2 and j >= 1:
            dp[i][j] += dp[i-2][j-1]
        if i >= 1 and j >= 2:
            dp[i][j] += dp[i-1][j-2]

print(dp[n-1][m-1])