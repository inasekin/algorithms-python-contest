"""
Задача о безопасных стопках контейнеров с радиоактивными материалами.

Есть 3 типа контейнеров: A (особо опасные), B (неопасные), C (совсем не опасные).
Стопка взрывоопасна, если в ней подряд идет более одного контейнера типа A.
Нужно найти количество безопасных (не взрывоопасных) вариантов стопки из N контейнеров.

Решение через динамическое программирование:
- dp[i][0] = количество способов составить стопку из i контейнеров, где последний НЕ типа A
- dp[i][1] = количество способов составить стопку из i контейнеров, где последний типа A

Переходы:
- dp[i][0] = (dp[i-1][0] + dp[i-1][1]) * 2
  (можем поставить B или C после любого контейнера)
- dp[i][1] = dp[i-1][0] * 1
  (можем поставить A только если предыдущий не A)

Начальные условия: dp[1][0] = 2 (B или C), dp[1][1] = 1 (A)
Ответ: dp[N][0] + dp[N][1]

Сложность: O(N)
"""

n = int(input())

dp = [[0, 0] for _ in range(n + 1)]
dp[1][0] = 2
dp[1][1] = 1

for i in range(2, n + 1):
    dp[i][0] = (dp[i-1][0] + dp[i-1][1]) * 2
    dp[i][1] = dp[i-1][0]

print(dp[n][0] + dp[n][1])