# Марсианские камни
#
# Задача: максимизировать количество образцов грунта при ограничении по весу
#
# Жадный алгоритм O(K log K):
# Чтобы взять как можно больше образцов при ограничении по весу,
# нужно брать самые легкие образцы
#
# Алгоритм:
# 1. Сортируем образцы по возрастанию веса
# 2. Берем образцы по порядку, пока суммарный вес не превысит N
# 3. Возвращаем количество взятых образцов

K, N = map(int, input().split())
weights = list(map(int, input().split()))

# Сортируем веса по возрастанию
weights.sort()

total_weight = 0
count = 0

# Жадно берем самые легкие образцы
for weight in weights:
    if total_weight + weight <= N:
        total_weight += weight
        count += 1
    else:
        break

print(count)