# Олимпиада по программированию
#
# Задача: максимизировать количество решенных задач
# Чтобы решить задачу i, нужен опыт >= a_i
# После решения задачи опыт увеличивается на b_i
#
# Жадный алгоритм O(N log N):
# 1. Сортируем задачи по возрастанию сложности
# 2. Решаем задачи в этом порядке, если хватает опыта
#
# Почему это оптимально:
# Если можем решить более сложную задачу, то всегда сможем решить более простую
# Поэтому выгодно начинать с самых простых задач

n, A = map(int, input().split())

tasks = []
for _ in range(n):
    a, b = map(int, input().split())
    tasks.append((a, b))

# Сортируем по сложности (по возрастанию)
tasks.sort()

current_exp = A
solved_count = 0

# Решаем задачи по порядку возрастания сложности
for difficulty, benefit in tasks:
    if current_exp >= difficulty:
        current_exp += benefit
        solved_count += 1

print(solved_count)