# Объединение отрезков
#
# Задача: объединить пересекающиеся отрезки и вывести минимальное количество отрезков
#
# Алгоритм O(N log N):
# 1. Сортируем отрезки по левой координате
# 2. Последовательно объединяем пересекающиеся и соприкасающиеся отрезки
# 3. Выводим результат

n = int(input())
segments = []

for _ in range(n):
    l, r = map(int, input().split())
    segments.append((l, r))

# Сортируем отрезки по левой координате
segments.sort()

# Объединяем пересекающиеся отрезки
merged = []
for l, r in segments:
    if merged and l <= merged[-1][1]:
        # Отрезки пересекаются или соприкасаются, объединяем
        merged[-1] = (merged[-1][0], max(merged[-1][1], r))
    else:
        # Новый непересекающийся отрезок
        merged.append((l, r))

# Выводим результат
print(len(merged))
for l, r in merged:
    print(l, r)