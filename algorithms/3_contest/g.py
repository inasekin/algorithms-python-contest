# Закраска прямой
#
# Задача: найти длину объединения отрезков на числовой прямой
#
# Алгоритм O(N log N):
# 1. Сортируем отрезки по левой координате
# 2. Объединяем пересекающиеся и соприкасающиеся отрезки
# 3. Суммируем длины объединенных отрезков
#
# Два отрезка [L1, R1] и [L2, R2] можно объединить, если R1 >= L2
# (при условии что L1 <= L2 после сортировки)

n = int(input())
segments = []

for _ in range(n):
    l, r = map(int, input().split())
    segments.append((l, r))

# Сортируем отрезки по левой координате
segments.sort()

# Объединяем пересекающиеся отрезки
merged = []
for l, r in segments:
    if merged and l <= merged[-1][1]:
        # Отрезки пересекаются или соприкасаются, объединяем
        merged[-1] = (merged[-1][0], max(merged[-1][1], r))
    else:
        # Новый непересекающийся отрезок
        merged.append((l, r))

# Считаем суммарную длину
total_length = 0
for l, r in merged:
    total_length += r - l

print(total_length)