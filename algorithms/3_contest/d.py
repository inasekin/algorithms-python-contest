# Уроки физкультуры
#
# Классическая задача о выборе интервалов (interval scheduling)
# Нужно выбрать максимальное количество непересекающихся интервалов
#
# Жадный алгоритм O(N log N):
# 1. Сортируем уроки по времени окончания
# 2. Выбираем жадно: берем урок с самым ранним окончанием,
#    затем следующий урок, который начинается не раньше окончания предыдущего
#
# Почему это оптимально:
# Выбирая уроки, которые заканчиваются раньше, мы оставляем больше времени
# для последующих уроков

n = int(input())
lessons = []

for _ in range(n):
    s, f = map(int, input().split())
    lessons.append((s, f))

# Сортируем по времени окончания
lessons.sort(key=lambda x: x[1])

selected = []
last_end = -1

# Жадно выбираем уроки
for start, end in lessons:
    # Если урок начинается не раньше окончания предыдущего
    if start >= last_end:
        selected.append((start, end))
        last_end = end

# Выводим результат
print(len(selected))
for start, end in selected:
    print(start, end)