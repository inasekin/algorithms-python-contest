# Задача C. Ровно k единиц
# Решение через итеративный подход со стеком с отсечением
# Подход: генерируем только векторы с ровно k единицами
# Используем стек для построения векторов, отсекаем неперспективные ветви
# Если единиц уже k - добавляем только нули, если не хватит мест для k единиц - отсекаем

n, k = map(int, input().split())

stack = [(0, '', 0)]
result = []

while stack:
    pos, current, ones = stack.pop()

    if pos == n:
        if ones == k:
            result.append(current)
        continue

    remaining = n - pos

    if ones < k and remaining >= k - ones:
        stack.append((pos + 1, current + '1', ones + 1))

    if ones <= k:
        stack.append((pos + 1, current + '0', ones))

for vec in result:
    print(vec)