# Задача B. Все строки длины n из k различных символов
# Решение через преобразование чисел в K-ичную систему счисления
# Подход: перебираем все числа от 0 до K^N - 1
# Каждое число представляем в K-ичной системе с N разрядами
# Это дает все возможные комбинации символов 0..K-1 длины N
# в лексикографическом порядке

n, k = map(int, input().split())

total = k ** n

for i in range(total):
    num = i
    result = []
    for _ in range(n):
        result.append(str(num % k))
        num //= k
    result.reverse()
    print(''.join(result))