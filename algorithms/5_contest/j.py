# Задача J. K-ая порядковая статистика
# Решение через сортировку массива
# Подход: считываем массив ростов, сортируем его
# выводим k-й элемент (с индексом k-1, так как индексация с 0)

n, k = map(int, input().split())
heights = list(map(int, input().split()))

heights.sort()

print(heights[k - 1])