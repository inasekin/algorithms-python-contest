# Найдите наибольшую правильную несократимую дробь, у которой сумма числителя и знаменателя равна n.

# В этом коде используются следующие алгоритмы и подходы:
#
#     Алгоритм Евклида для нахождения НОД
#         Реализован в функции gcd(a, b).
#         Классический алгоритм Евклида: на каждом шаге большее число заменяется остатком
#         от деления большего на меньшее, пока одно из чисел не станет равным нулю.
#         Используется для проверки взаимной простоты чисел (НОД = 1 означает несократимую дробь).
#     Алгоритм перебора с убыванием (Brute Force / Iteration)
#         Реализован в функции findFraction(n).
#         Перебираем возможные числители, начиная с максимального ((n-1)//2) и двигаясь вниз.
#         Это гарантирует, что первая найденная подходящая дробь будет наибольшей.
#     Проверка условий правильной несократимой дроби
#         Для каждой пары (числитель, знаменатель):
#           - Проверяем, что числитель < знаменателя (правильная дробь)
#           - Проверяем, что НОД(числитель, знаменатель) = 1 (несократимая дробь)
#         Как только находим подходящую пару, возвращаем её.
#
# Кратко: Код использует перебор числителей от максимального к минимальному,
# а для каждой пары применяет алгоритм Евклида для проверки несократимости дроби.

def gcd(a, b):
    while b:
        a, b = b, a % b
    return a


def findFraction(n):
    for numerator in range((n - 1) // 2, 0, -1):
        denominator = n - numerator

        if numerator < denominator:
            if gcd(numerator, denominator) == 1:
                return f"{numerator} {denominator}"

    return ""

print(findFraction(int(input())))