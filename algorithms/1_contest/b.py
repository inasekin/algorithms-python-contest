# Выведите все числа в диапазоне от 2 до N, у которых есть хотя бы три различных простых делителя.

# В этом коде используются следующие алгоритмы и подходы:
#
#     Алгоритм факторизации (разложения на простые множители)
#         Реализован в функции getPrimes(n).
#         Алгоритм последовательно перебирает потенциальные простые делители (d), начиная с 2.
#         Он эффективно находит все уникальные простые множители заданного числа n, используя оптимизацию d * d <= n (достаточно проверять делители только до корня из числа).
#         Он не просто находит один делитель, а полностью "выдавливает" (делит нацело, n //= d) каждый найденный простой множитель из n, чтобы гарантировать, что в итоговый список result попадет только один экземпюр каждого уникального простого делителя.
#     Алгоритм перебора (Brute Force / Iteration)
#         Реализован в функции threePrime(n).
#         Эта функция использует цикл for num in range(2, n + 1), который последовательно перебирает каждое целое число в заданном диапазоне от 2 до N.
#     Подсчет уникальных простых делителей и фильтрация
#         Внутри цикла перебора для каждого числа num вызывается функция getPrimes(num), чтобы получить список его уникальных простых делителей.
#         Затем проверяется условие len(getPrimes(num)) >= 3 (длина списка должна быть не менее 3).
#         Если условие выполняется, число добавляется в итоговый список.
#
# Кратко: Код использует алгоритм перебора для проверки всех чисел в диапазоне, а для каждого числа применяет оптимизированный алгоритм факторизации для подсчета количества его уникальных простых делителей.

def getPrimes(n):
    result = []
    d = 2
    while d * d <= n:
        if n % d == 0:
            result.append(d)
            while n % d == 0:
                n //= d
        d += 1
    if n > 1:
        result.append(n)

    return result

def threePrime(n):
    result = []
    for num in range(2, n + 1):
        if len(getPrimes(num)) >= 3:
            result.append(num)
    return " ".join(map(str, result))

print(threePrime(int(input())))