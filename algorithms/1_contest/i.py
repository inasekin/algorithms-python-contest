# Найти все пары дружественных чисел в диапазоне от M до N.

# В этом коде используются следующие алгоритмы и подходы:
#
#     Предвычисление сумм делителей (решето-подобный подход)
#         Создаем массив sum_div для всех чисел до N.
#         Для каждого делителя d от 1 до N добавляем d ко всем его кратным.
#         Затем вычитаем само число (нужны только собственные делители).
#         Сложность: O(N log N) - намного быстрее, чем вычислять для каждого числа отдельно.
#     Проверка на дружественность
#         Для каждого числа a проверяем: если sum_div[a] = b и sum_div[b] = a, то пара дружественная.
#     Избежание дубликатов
#         Выводим пару только если a < b.
#
# Кратко: Код один раз вычисляет суммы делителей для всех чисел до N,
# затем быстро проверяет условия дружественности.

m, n = map(int, input().split())

sum_div = [0] * (n + 1)

for d in range(1, n + 1):
    for multiple in range(d, n + 1, d):
        sum_div[multiple] += d

for i in range(n + 1):
    sum_div[i] -= i

result = []

for a in range(m, n + 1):
    b = sum_div[a]

    if m <= b <= n and sum_div[b] == a and a < b:
        result.append(f"{a} {b}")

if result:
    print("\n".join(result))
else:
    print("Absent")