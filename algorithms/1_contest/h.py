# Даны две сцепленные шестеренки. У одной N зубцов, у другой K.
# Требуется найти минимальное число поворотов на один зубчик, чтобы вернуться в исходное состояние.

# В этом коде используются следующие алгоритмы и подходы:
#
#     Алгоритм Евклида (Euclidean Algorithm)
#         Реализован в функции gcd(a, b).
#         Эффективно находит наибольший общий делитель (НОД) двух чисел.
#         Используется многократное вычисление остатка от деления до тех пор, пока остаток не станет равен нулю.
#     Формула для вычисления наименьшего общего кратного (НОК)
#         НОК(N, K) = N * K / НОД(N, K)
#         Шестеренки возвращаются в исходное положение, когда обе прошли НОК зубчиков.
#
# Кратко: Код использует алгоритм Евклида для нахождения НОД, затем вычисляет НОК
# по формуле, что и дает минимальное количество поворотов для возврата в исходное состояние.

n, k = map(int, input().split())

def gcd(a, b):
    while b:
        a, b = b, a % b
    return a

def lcm(n, k):
    return (n // gcd(n, k)) * k

print(lcm(n, k))