# Даны два целых числа l и r. Посчитайте общее количество различных простых делителей
# всех целых чисел между l и r включительно.

# В этом коде используются следующие алгоритмы и подходы:
#
#     Решето Эратосфена
#         Генерируем все простые числа до r.
#         Для каждого простого p проверяем, делит ли оно хотя бы одно число в [l, r].
#     Проверка делимости в диапазоне
#         Для каждого простого p находим первое кратное >= l.
#         Если это кратное <= r, значит p является делителем для чисел в диапазоне.
#     Подсчет уникальных простых делителей
#         Считаем количество простых, которые делят хотя бы одно число в [l, r].
#
# Кратко: Код генерирует все простые до r с помощью решета, затем для каждого
# простого проверяет, есть ли в диапазоне [l, r] число, которое делится на него.

l, r = map(int, input().split())

is_prime = [True] * (r + 1)
is_prime[0] = is_prime[1] = False

p = 2
while p * p <= r:
    if is_prime[p]:
        for i in range(p * p, r + 1, p):
            is_prime[i] = False
    p += 1

count = 0
for p in range(2, r + 1):
    if is_prime[p]:
        first = ((l + p - 1) // p) * p
        if first <= r:
            count += 1

print(count)