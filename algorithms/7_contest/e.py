"""
Задача о рюкзаке с восстановлением ответа (0-1 Knapsack Problem).

Дано N предметов с массами и стоимостями, нужно выбрать набор предметов
с максимальной суммарной стоимостью так, чтобы их суммарный вес не превышал M.

Решение через динамическое программирование:
- dp[i][w] = максимальная стоимость при рассмотрении первых i предметов и весе w
- Для каждого предмета есть два варианта: взять его или не брать
- Если берем предмет i: dp[i][w] = dp[i-1][w-mass[i]] + cost[i]
- Если не берем: dp[i][w] = dp[i-1][w]
- Выбираем максимум из двух вариантов

Для восстановления ответа идем от dp[N][M] обратно и проверяем,
был ли взят каждый предмет (изменилась ли стоимость после его добавления).

Сложность: O(N * M)
"""

n, m = map(int, input().split())
masses = list(map(int, input().split()))
costs = list(map(int, input().split()))

dp = [[0] * (m + 1) for _ in range(n + 1)]

for i in range(1, n + 1):
    for w in range(m + 1):
        dp[i][w] = dp[i-1][w]
        if masses[i-1] <= w:
            dp[i][w] = max(dp[i][w], dp[i-1][w - masses[i-1]] + costs[i-1])

result = []
w = m
for i in range(n, 0, -1):
    if dp[i][w] != dp[i-1][w]:
        result.append(i)
        w -= masses[i-1]

result.reverse()
print(' '.join(map(str, result)))